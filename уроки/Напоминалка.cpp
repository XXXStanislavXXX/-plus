#include <iostream>

/*
Задача 4. Напоминалка.

Василий — очень ответственный менеджер, но очень забывчивый. Иногда он пьет воду
несколько раз подряд, потому что забыл, что только что пил. Напишите для него
программу-напоминалку. В самом начале программа спрашивает, сколько раз ему
напомнить, а затем нужное количество раз выводит “Вы хотели не забыть о чем-то”.

Усложнение: добавьте возможность ввести фразу-напоминалку, чтобы можно было
напоминать о чем-то конкретном.
*/

int main() {
  std::string reminder;
  int count;
  std::cout << "Введите напоминалку: ";
  std::getline(std::cin, reminder);
  std::cout << "Сколько раз напомнить? ";
  std::cin >> count;
  do{
    std::cout << reminder << std::endl;
  }while(--count > 0);
  std::cout << "Программа завершила свою работу." << std::endl;
}
